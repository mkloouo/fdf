#include <vector.h>

t_vec3f					*cross_vec3f(t_vec3f *a, t_vec3f *b)
{
	return (get_vec3f((a->x * b->z - a->z * b->y),
						(a->z * b->x - a->x * b->z),
						(a->x * b->y - a->y * b->x)));
}

